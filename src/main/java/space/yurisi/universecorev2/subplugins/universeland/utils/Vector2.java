package space.yurisi.universecorev2.subplugins.universeland.utils;

public class Vector2 {

    public int x;
    public int z;

    public Vector2(int x, int z) {
        this.x = x;
        this.z = z;
    }

    public int getX() {
        return x;
    }

    public int getZ() {
        return z;
    }
}
